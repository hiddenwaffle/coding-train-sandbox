import { Sketch } from '../../handcar'
const s = new Sketch()
s.size(400, 400)

s.draw = () => {
  s.background(0)
  s.translate(200, 200)
  s.rotate(s.radians(-90))
  const sc = s.second()
  const mn = s.minute()
  const hr = s.hour()
  s.noFill()
  s.strokeWeight(8)
  s.stroke(204, 0, 53)
  const scAngle = s.map(sc, 0, 60, 0, 360)
  s.arc(0, 0, 300, 300, 0, s.radians(scAngle))
  s.pushMatrix()
  s.rotate(s.radians(scAngle))
  s.line(0, 0, 100, 0)
  s.popMatrix()
  s.stroke(53, 76, 161)
  const mnAngle = s.map(mn, 0, 60, 0, 360)
  s.arc(0, 0, 280, 280, 0, s.radians(mnAngle))
  s.pushMatrix()
  s.rotate(s.radians(mnAngle))
  s.line(0, 0, 75, 0)
  s.popMatrix()
  const hrAngle = s.map(hr % 12, 0, 12, 0, 360)
  s.stroke(255)
  s.arc(0, 0, 260, 260, 0, s.radians(hrAngle))
  s.pushMatrix()
  s.rotate(s.radians(hrAngle))
  s.line(0, 0, 50, 0)
  s.popMatrix()
}
